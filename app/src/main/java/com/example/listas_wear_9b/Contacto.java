package com.example.listas_wear_9b;

public class Contacto {

    private int image;
    private String name;

    public Contacto(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
